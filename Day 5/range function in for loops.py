# printing first 10 natural numbers :
for i in range(1, 11):
    print(i)

print()

# step argument in range function :
for i in range(1, 11, 2):
    print(i)

print()

# printing the sum of first 100 natural numbers :
total = 0
for number in range(1, 101):
    total += number
print(total)
