# variable names must be sensible :
name = "Angela Yu"
print(name)


# variable names should start with an alphabet and can start with an underscore :
language1 = "Python"
print(language1)

_text = "I love programming!"
print(_text)


# variable names cannot start with a number or any special character :
# 1language = "Golang" # SyntaxError


# variable names with multiple words can be seperated by underscores (SNAKE CASE) :
full_name = "Jennifer Olive"
print(full_name)


# reserved words cannot be used as a variable name :
# continue = "False" # SyntaxError


# variable names are case sensetive :
gender = "Male"
print(gender)

Gender = "Female"
print(Gender)
