# addition +
print("2 + 3 :", 2 + 3)

# subtraction :
print("5 - 2 :", 5 - 2)

# division /
print("15 / 5 :", 15 / 5)

# multiplication *
print("5 * 2 :", 5 * 2)

# exponentiation **
print("2 ** 3 :", 2**3)

# floor division //
print("15 // 5 :", 15 // 5)

# Modulo Operator
print("5 % 5:", 5 % 5)
