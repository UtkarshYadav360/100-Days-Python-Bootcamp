# strings :
text = "Text"
print(text)

# accessing elements of the strings :
print(text[0])
print(text[1])
print(text[2])
print(text[3])

# concatinating numbered strings :
print("123" + "456")


# Integers :
age = 18
print(age)

# adding numbers
print(123 + 456)

# large integers can be seperated using underscores
annual_income = 596_682_980
print(annual_income)


# Float
body_temperature = 90.6
print(body_temperature)

# adding floating point numbers
print(2.56 + 0.4)


# Boolean :
genius = True
print(genius)

dumb = False
print(dumb)
